public class AR_multiplication
{

    public static void main(String[] args)
    {
        int a=12;
        for(int i=1; i<=10 ;i++)
        {
            System.out.println(""+a+" * "+i+"= "+ (a*i));

        }
        System.out.println("table of 10 in reverse");
        for(int j=10;j>=1;j--)
        {
            System.out.println("10 * "+j+ "= "+(j*10));
        }
    }
}
